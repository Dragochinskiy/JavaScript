/**
 * Функция складывает параметр "а" и "b"
 * @param {number} a 
 * @param {number} b 
 * @returns {number}
 */
function plus(a, b) {
    return a + b;
}

/**
 * Функция из параметра "a" вычитает "b"
 * @param {number} a 
 * @param {number} b 
 * @returns {number}
 */
function minus(a, b) {
    return a - b;
}

/**
 * Функция умножает параметр "a" на "b"
 * @param {number} a 
 * @param {number} b 
 * @returns {number}
 */
function umnozhit(a, b) {
    return a * b;
}

/**
 * Функция делит параметр "a" на "b"
 * @param {number} a 
 * @param {number} b 
 * @returns {number}
 */
function razdelit(a, b) {
    return a / b;
}